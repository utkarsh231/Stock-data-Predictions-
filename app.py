import numpy as np
from numpy import vectorize
import pandas as pd
from flask import Flask, request, render_template
from nltk.sentiment.vader import SentimentIntensityAnalyzer
import unicodedata
import keras as K
import pickle
from textblob import TextBlob
# instantiating the Sentiment Analyzer
sid = SentimentIntensityAnalyzer()

from keras.models import load_model

with open('finalized_model.pkl', 'rb') as f:
    model = pickle.load(f)


app = Flask(__name__)



@app.route('/')
def view():
    return render_template('form.html')


@app.route('/predict', methods=['GET','POST'])
def predict():
    if request.method=="POST":
        review = request.form['text']
    
        blob = TextBlob(review)
        for sentence in blob.sentences:
            compound_value = sentence.sentiment.polarity
        value = request.form['open']
        value = float(value)

        test = np.array([compound_value,value])
        test = test.reshape(1,-1)

        output = model.predict(test)

        output = output[0]
        out = output.astype(int)
        
        
       
        if ( out == 1):
            prediction_text = "STOCK WILL RISE"
        elif (out == 0):
            prediction_text = "STOCK WILL FALL"
    
         

        return render_template('form.html', prediction_text = prediction_text )

    else:
        return render_template('form.html')


        

   
if __name__ == "__main__":
    app.debug = True
    app.run()

